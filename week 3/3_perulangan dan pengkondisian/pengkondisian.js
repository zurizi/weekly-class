//if
let angka = 10;

if (angka > 0) {
  console.log("Angka adalah bilangan positif");
}

//else if
let angka = -5;

if (angka > 0) {
  console.log("Angka adalah bilangan positif");
} else {
  console.log("Angka adalah bilangan negatif atau nol");
}


//else
let angka = 0;

if (angka > 0) {
  console.log("Angka adalah bilangan positif");
} else if (angka < 0) {
  console.log("Angka adalah bilangan negatif");
} else {
  console.log("Angka adalah nol");
}
